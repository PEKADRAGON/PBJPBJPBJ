end

function removeHex (s)
    return s:gsub ("#%x%x%x%x%x%x", "") or false
end

function minutosParaHoras(minutos)
    local horas = math.floor(minutos / 60)
    local minutosRestantes = minutos % 60
    return horas, minutosRestantes
end

function timestampToDateString(timestamp)
    local dateTable = os.date("*t", timestamp)
    local dateString = string.format("%02d/%02d/%04d", dateTable.day, dateTable.month, dateTable.year)
    return dateString
end

