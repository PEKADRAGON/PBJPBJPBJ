function formatNumber(number, sep)
	assert(type(tonumber(number))=="number", "Bad argument @'formatNumber' [Expected number at argument 1 got "..type(number).."]")
	assert(not sep or type(sep)=="string", "Bad argument @'formatNumber' [Expected string at argument 2 got "..type(sep).."]")
	sep = sep or '.'
	local money = number
	for i = 1, tostring(money):len()/3 do
		money = string.gsub(money, "^(-?%d+)(%d%d%d)", "%1"..sep.."%2")
	end
	return money
end

function getPlayerVips ( player )	
	if not player then 
		return false 
	end
	local account = getAccountName ( getPlayerAccount ( player) ) 
	local infos = {}
	for i, v in ipairs ( config["vips"] ) do 
		if (aclGetGroup(v)) then 
			if (isObjectInACLGroup("user."..account, aclGetGroup (v))) then 
				local time = exports["guetto_gerenciadorVip"]:getTimeVip(player, v);
				infos[v] = {true, time}  
			end
		end
	end
	return infos
end

function registerEventHandler(event, ...)
