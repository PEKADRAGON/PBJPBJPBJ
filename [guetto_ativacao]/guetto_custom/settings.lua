function setNewCharacterData(player, typeData, category, id)
    local character = getElementData(player, "characterClothes")
    local genero = getElementData(player, "characterGenre")
    if typeData == "clothe" then
        character[genero]["clothes"][category] = id
    elseif typeData == "face" then
        character[genero]["face"][category] = id
    elseif typeData == "tatoo" then
        character[genero]["tatoo"][category] = id
    elseif typeData == "hair" then
        character[genero]["hair"][category] = id
    end
    setElementData(player, "characterClothes", character)
end

